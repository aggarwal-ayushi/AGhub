package pkg2;

public class switcheg {
	public static void main(String[] args) {
		int num = 1;
		switch(num)
		{
		case 0: System.out.println("10");
		break;
		case 1: System.out.println("20");
		break;
		case 2: System.out.println("no");
		break;
		default:System.out.println("not");
		}
	}

}
